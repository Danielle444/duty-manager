function HelpSectionCard({ section }: { section: HelpSection }) {
  return (
    <details className="group rounded-xl border border-border bg-card p-4 open:pb-4">
      <summary className="cursor-pointer list-none text-base font-bold text-card-foreground marker:content-none">
        <span className="flex items-center justify-between gap-2">
          {section.title}
          <span className="text-muted-foreground transition-transform group-open:rotate-180">⌄</span>
        </span>
      </summary>
      <div className="mt-2 flex flex-col gap-1.5 text-sm text-muted-foreground">
        {section.body.map((paragraph) => (
          <p key={paragraph}>{paragraph}</p>
        ))}
      </div>
    </details>
  );
}

export function HelpContent({ role }: { role: "admin" | "instructor" | "student" }) {
  const sections =
    role === "admin"
      ? ADMIN_HELP_SECTIONS
      : role === "instructor"
        ? INSTRUCTOR_HELP_SECTIONS
        : STUDENT_HELP_SECTIONS;

  return (
    <div className="flex flex-col gap-4">
      <div>
        <h1 className="text-xl font-bold text-card-foreground">מדריך שימוש</h1>
        <p className="mt-1 text-sm text-muted-foreground">
          כאן אפשר למצוא הסבר קצר על המסכים המרכזיים במערכת.
        </p>
      </div>
      <div className="flex flex-col gap-3">
        {sections.map((section) => (
          <HelpSectionCard key={section.title} section={section} />
        ))}
      </div>
    </div>
  );
}
